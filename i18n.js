export const translateUiText = (value, language = 'en', node = null) => {
  if (language !== 'nl') return String(value ?? '');
  const { before, core, after } = splitOuterWhitespace(value);
  if (!core || /^(?:https?:\/\/|mailto:|www\.)/i.test(core)) return String(value ?? '');
  if (EXACT_NL[core]) return `${before}${EXACT_NL[core]}${after}`;
  let result = dynamicTranslate(core);
  if (result !== core) return `${before}${result}${after}`;
  if (likelyPolish(core, node) || likelyDutch(core)) return String(value ?? '');

  for (const [english, dutch] of PHRASE_NL) {
    const pattern = new RegExp(`\\b${english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'gi');
    result = result.replace(pattern, (match) => preserveCase(match, dutch));
  }

  result = result.replace(/\b[A-Za-z][A-Za-z'-]*\b/g, (word) => {
    if (ALLOWED_ENGLISH.has(word)) return word;
    const lower = word.toLowerCase();
    const translated = WORD_NL[lower];
    return translated ? preserveCase(word, translated) : word;
  });
  return `${before}${result}${after}`;
};

